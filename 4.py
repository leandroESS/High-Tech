# 4-Faça um Programa que peça as 4 notas bimestrais e mostre a média.

print("Digite a primeira nota bimestral")
nota1 = int(input())
print("Digite a segunda nota bimestral")
nota2 = int(input())
print("Digite a terceira nota bimestral")
nota3 = int(input())
print("Digite a quarta nota bimestral")
nota4 = int(input())

media = (nota1 + nota2 + nota3 + nota4)/4
print(f"O média do bimestre é {media}")